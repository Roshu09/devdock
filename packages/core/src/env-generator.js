import { writeFileSync, existsSync, readFileSync } from 'fs';
import { join } from 'path';

export class EnvGenerator {
  constructor(projectPath, analysisResult, serviceResults) {
    this.projectPath = projectPath;
    this.analysis = analysisResult;
    this.services = serviceResults; // ports from docker manager
    this.envPath = join(projectPath, '.env');
  }

  generate() {
    // Don't overwrite existing .env — too dangerous
    if (existsSync(this.envPath)) {
      return { created: false, path: this.envPath, reason: 'already exists' };
    }

    const vars = { ...this.analysis.envVars };

    // Auto-fill service connection vars based on what we spun up
    for (const service of this.services) {
      const port = service.port;

      if (service.service === 'postgres') {
        vars['DB_HOST'] = vars['DB_HOST'] || 'localhost';
        vars['DB_PORT'] = vars['DB_PORT'] || String(port);
        vars['DB_USER'] = vars['DB_USER'] || 'devdock';
        vars['DB_PASSWORD'] = vars['DB_PASSWORD'] || 'devdock';
        vars['DB_NAME'] = vars['DB_NAME'] || this.analysis.name;
        vars['DATABASE_URL'] = vars['DATABASE_URL'] ||
          `postgresql://devdock:devdock@localhost:${port}/${this.analysis.name}`;
      }

      if (service.service === 'redis') {
        vars['REDIS_HOST'] = vars['REDIS_HOST'] || 'localhost';
        vars['REDIS_PORT'] = vars['REDIS_PORT'] || String(port);
        vars['REDIS_URL'] = vars['REDIS_URL'] || `redis://localhost:${port}`;
      }

      if (service.service === 'mongodb') {
        vars['MONGODB_URI'] = vars['MONGODB_URI'] ||
          `mongodb://devdock:devdock@localhost:${port}/devdock`;
      }

      if (service.service === 'mysql') {
        vars['DB_HOST'] = vars['DB_HOST'] || 'localhost';
        vars['DB_PORT'] = vars['DB_PORT'] || String(port);
        vars['DB_USER'] = vars['DB_USER'] || 'devdock';
        vars['DB_PASSWORD'] = vars['DB_PASSWORD'] || 'devdock';
        vars['DB_NAME'] = vars['DB_NAME'] || this.analysis.name;
      }
    }

    // Fill common defaults for empty vars
    const defaults = {
      NODE_ENV: 'development',
      PORT: '3000',
      JWT_SECRET: this._generateSecret(),
      JWT_ACCESS_SECRET: this._generateSecret(),
      JWT_REFRESH_SECRET: this._generateSecret(),
    };

    for (const [key, val] of Object.entries(vars)) {
      if (!val && defaults[key]) {
        vars[key] = defaults[key];
      }
    }

    // Write .env file
    const lines = [
      '# Generated by devdock 🐳',
      `# Project: ${this.analysis.name}`,
      `# Created: ${new Date().toISOString()}`,
      '',
      ...Object.entries(vars).map(([k, v]) => `${k}=${v}`)
    ];

    writeFileSync(this.envPath, lines.join('\n'));
    return { created: true, path: this.envPath, vars };
  }

  _generateSecret() {
    return [...Array(48)].map(() =>
      Math.random().toString(36)[2] || '0'
    ).join('');
  }
}
